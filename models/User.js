const mongose= require ('mongoose');

const UserSchema= new mongose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    avatar: {
        type: String
    },
    date: {
        type: Date,
        default: Date.now
    },
});

module.exports = User = mongose.model('User',UserSchema);